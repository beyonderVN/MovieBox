    @Override
    public void doWork(int work, Object args) {
        switch (work){
            case ChatApplication.SIGN_IN_SUCCESS:
                Intent intent = new Intent();
                setResult(RESULT_OK, intent);
                finish();
                break;
            case ChatApplication.SIGNIN_FAIL:
                Toast.makeText(mActivity, getString(com.vinhsang.demo.R.string.error_login), Toast.LENGTH_SHORT).show();
                showProgress(false);
                break;
        }
        return;
    }


